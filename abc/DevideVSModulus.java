
public class DevideVSModulus {

	public static void main(String[] args) {

		// Dividend
		int a = 15;

		// Divisor
		int b = 8;

		//Divided result
		int devided = a / b;
		System.out.println("Divided result "+ devided);
		
		// Mod
		int mod = a % b;
		System.out.println("Modulu result " + mod);
	}

}
